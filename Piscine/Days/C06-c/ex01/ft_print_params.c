#include <unistd.h>

int	main(int ac, char **argv)
{
	int	i;
	int	j;

	j = 0;
	i = 1;
	while (i < ac)
	{
		j = 0;
		while (argv[i][j] != '\0')
		{
			write(1, &argv[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
	return (0);
}
