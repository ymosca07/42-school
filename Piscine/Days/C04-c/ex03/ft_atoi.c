int	ft_atoi(char *str)
{
	int	sign;
	int	i;
	int	sum;

	sign = 1;
	i = 0;
	sum = 0;
	while (str[i] != '\0')
	{
		while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
			i++;
		while (str[i] == '-' || str[i] == '+')
		{
			if (str[i] == '-')
				sign *= -1;
			i++;
		}
		while (str[i] >= '0' && str[i] <= '9')
		{
			sum = (sum * 10) + str[i] - 48;
			i++;
		}
		return (sum * sign);
	}
	return (0);
}
