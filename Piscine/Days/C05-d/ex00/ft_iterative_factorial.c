int	ft_iterative_factorial(int nb)
{
	int	i;
	int	j;

	i = 1;
	j = 1;
	if (nb == 0)
		return (1);
	if (nb < 1)
		return (0);
	while (i <= nb)
	{
		j *= i;
		i++;
	}
	return (j);
}
