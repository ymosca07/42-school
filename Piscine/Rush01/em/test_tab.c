#include <unistd.h>

int main ()
{
	int col;
	int line;
	int i;
	
	
	col = 0;
	line = 0;
	i = 0;
	
	while (line < 4)
	{
		col = 0;
		while (col < 4)
		{
			write (1, "0 ", 2);
			col ++;
		}
	write (1, "\n",1);
	line++;
	}
}
